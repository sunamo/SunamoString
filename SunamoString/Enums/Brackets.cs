namespace SunamoString.Enums;

/// <summary>
/// Defines the types of brackets used in string operations.
/// </summary>
public enum Brackets
{
    /// <summary>
    /// Curly braces { }.
    /// </summary>
    Curly,
    /// <summary>
    /// Square brackets [ ].
    /// </summary>
    Square,
    /// <summary>
    /// Normal parentheses ( ).
    /// </summary>
    Normal,
    /// <summary>
    /// No brackets.
    /// </summary>
    None
}